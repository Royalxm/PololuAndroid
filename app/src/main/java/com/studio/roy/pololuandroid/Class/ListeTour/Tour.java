public class Tour {

    String id;
    String NameTour;
    String EtatTour;
    String DateTour;

    public Tour() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNameTour() {
        return NameTour;
    }

    public void setNameTour(String nameTour) {
        NameTour = nameTour;
    }

    public String getEtatTour() {
        return EtatTour;
    }

    public void setEtatTour(String etatTour) {
        EtatTour = etatTour;
    }

    public String getDateTour() {
        return DateTour;
    }

    public void setDateTour(String dateTour) {
        DateTour = dateTour;
    }
}
